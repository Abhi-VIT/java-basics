public class App_Relational_operator {
    public static void main(String[] args){
        int num1= 10 , num2=5;
        System.out.println(num1>num2);
        System.out.println(num1<num2);
        System.out.println(num1>=num2);
        System.out.println(num1<=num2);
        System.out.println(num1==num2);
        System.out.println(num1!=num2);
    }
}